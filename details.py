
# user-defined function to display employee details
def display_employee(fullname, position, age, gender):
    print("Full Name:", fullname)
    print("Position:", position)
    print("Age:", age)
    print("Gender:", gender)
    print("-" * 30)     #causes the break

# calling the function with arguments (five employees)
display_employee("Glory Akinyi", "Trainer", 35, "Female")
