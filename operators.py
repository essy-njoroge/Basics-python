  #arithmetic operators - these are for simple calculations

X = 34
Y = 20

print(X + Y)
print(X - Y)
print(X * Y)
print(X / Y)
print(X % Y)    #modulus - returns the remainder

#comparison operators - they compare values - less than

print(X < Y)
print(X > Y)
print(X <= Y)
print(X >= Y)
print(X == Y)   #EQUAL TO
print(X != Y)   #NOT EQUAL TO

#assignment operators
number = 10
print(number)

#operator precedence
number += 2
print(number)

#logical operators - and,or,not
num1 = 60
num2 = 12

print(num1 == num2 and num1 > num2)
print(num1 == num2 or num1 > num2)
print(not(num1 == num2 or num1 > num2))   #to get the opposite of something

